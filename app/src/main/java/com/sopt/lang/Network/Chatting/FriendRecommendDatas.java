public class FriendRecommendDatas {
    public String user_id;
    public String user_image;
    public String user_name;
    public String native_lang;
    public String hope_lang;


    public FriendRecommendDatas(String user_id, String user_image, String user_name, String native_lang, String hope_lang) {
        this.user_id = user_id;
        this.user_image = user_image;
        this.user_name = user_name;
        this.native_lang = native_lang;
        this.hope_lang = hope_lang;
    }

}
